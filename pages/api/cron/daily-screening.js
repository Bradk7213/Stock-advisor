// pages/api/cron/daily-screening.js
// Runs daily at 8am via Vercel Cron — generates stock ideas and notifies teams
import { createClient } from '@supabase/supabase-js';
import Anthropic from '@anthropic-ai/sdk';
import axios from 'axios';

const supabase = createClient(
  process.env.SUPABASE_URL,
  process.env.SUPABASE_SERVICE_ROLE_KEY
);

const anthropic = new Anthropic({
  apiKey: process.env.ANTHROPIC_API_KEY,
});

const TEAMS_WEBHOOK_URL = process.env.TEAMS_WEBHOOK_URL;

function getScreeningPrompt(category) {
  const prompts = {
    'trending': "Find 2-3 US stocks that are trending TODAY with momentum. Include ticker, why it's trending, and a Buy/Hold/Sell stance.",
    'undervalued': "Identify 2-3 undervalued US stocks with strong fundamentals trading below fair value. Include ticker, valuation metrics, and why it's undervalued.",
    'high-growth': "Recommend 2-3 high-growth US stocks with strong upside. Include ticker, growth story, and risk/reward analysis."
  };
  return (prompts[category] || prompts['trending']) + '\n\nBe concise. Include specific tickers and numbers. Format as bullet points.';
}

async function runDailyScreening() {
  // Log job start
  await supabase.from('job_logs').insert({
    job_type: 'daily_screening',
    status: 'started'
  });

  const categories = ['trending', 'undervalued', 'high-growth'];
  const allIdeas = {};

  for (const category of categories) {
    console.log(`Screening for ${category} stocks...`);
    const message = await anthropic.messages.create({
      model: 'claude-sonnet-4-20250514',
      max_tokens: 1500,
      messages: [{ role: 'user', content: getScreeningPrompt(category) }]
    });
    allIdeas[category] = message.content[0].type === 'text' ? message.content[0].text : '';
  }

  // Get all teams
  const { data: teams } = await supabase.from('teams').select('id, name');

  for (const team of teams || []) {
    const today = new Date().toISOString().split('T')[0];

    // Save screening result
    const { error } = await supabase.from('screening_results').insert({
      team_id: team.id,
      screening_date: today,
      category: 'daily',
      stocks: allIdeas,
      sent_to_teams: false,
      sent_to_email: false
    });

    if (!error) {
      // Send to Microsoft Teams if configured
      if (TEAMS_WEBHOOK_URL) {
        await sendToTeams(team, allIdeas, today);
      }
      // Queue email digest for team members
      await queueEmailDigest(team.id, allIdeas);
    }
  }

  // Log completion
  await supabase.from('job_logs').insert({
    job_type: 'daily_screening',
    status: 'completed',
    completed_at: new Date()
  });

  console.log('Daily screening completed successfully');
  return { success: true, timestamp: new Date() };
}

async function sendToTeams(team, ideas, today) {
  try {
    const message = {
      '@type': 'MessageCard',
      '@context': 'https://schema.org/extensions',
      summary: `Stock Advisor Daily Screening — ${today}`,
      themeColor: '0078D4',
      sections: [
        {
          activityTitle: '📈 Daily Stock Ideas',
          activitySubtitle: team.name || 'Your Team',
          text: formatIdeasForTeams(ideas),
          markdown: true
        }
      ]
    };

    await axios.post(TEAMS_WEBHOOK_URL, message);

    await supabase
      .from('screening_results')
      .update({ sent_to_teams: true })
      .eq('team_id', team.id)
      .eq('screening_date', today);

    console.log(`Sent to Teams for team ${team.id}`);
  } catch (error) {
    console.error('Teams webhook error:', error.message);
  }
}

function formatIdeasForTeams(ideas) {
  return Object.entries(ideas)
    .map(([category, content]) => `**${category.toUpperCase().replace('-', ' ')}**\n${content}`)
    .join('\n\n');
}

async function queueEmailDigest(teamId, ideas) {
  try {
    const { data: users } = await supabase
      .from('users')
      .select('id, email')
      .eq('team_id', teamId);

    if (!users || users.length === 0) return;

    const emailContent = formatIdeasForEmail(ideas);

    for (const user of users) {
      await supabase.from('notifications').insert({
        team_id: teamId,
        user_id: user.id,
        type: 'daily_screening',
        recipient_email: user.email,
        content: emailContent,
        status: 'pending'
      });
    }
  } catch (error) {
    console.error('Queue email error:', error.message);
  }
}

function formatIdeasForEmail(ideas) {
  let html = `<h2>📈 Your Daily Stock Ideas</h2>`;
  for (const [category, content] of Object.entries(ideas)) {
    html += `<h3>${category.toUpperCase().replace('-', ' ')}</h3>`;
    html += `<p>${content.replace(/\n/g, '<br>')}</p>`;
  }
  html += `<p><em>Generated by Stock Advisor on ${new Date().toLocaleDateString()}</em></p>`;
  return html;
}

export default async function handler(req, res) {
  // Verify this is from Vercel Cron
  if (req.headers.authorization !== `Bearer ${process.env.CRON_SECRET}`) {
    return res.status(401).json({ error: 'Unauthorized' });
  }

  try {
    const result = await runDailyScreening();
    return res.status(200).json(result);
  } catch (error) {
    console.error('Daily screening failed:', error);

    await supabase.from('job_logs').insert({
      job_type: 'daily_screening',
      status: 'failed',
      error_message: error.message,
      completed_at: new Date()
    }).catch(() => {});

    return res.status(500).json({ error: error.message });
  }
}
